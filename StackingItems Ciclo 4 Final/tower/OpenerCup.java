package tower;

/**
 * Representa una taza abrelatas. Al ser ingresada a la torre, elimina y destruye visualmente todas las tapas de la pila.
 */
public class OpenerCup extends Cup {
    /**
     * Constructor de la OpenerCup.
     * @param size El tamaño lógico de la taza.
     * @param color El color visual de la taza.
     */
    public OpenerCup(int size, String color) { super(size, color); }
}